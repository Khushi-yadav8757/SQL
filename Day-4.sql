ğŸ”¹ 1. DDL (Data Definition Language)
Structure define karne ke liye

Command	Use
CREATE	Nayi table ya database banane ke liye
ALTER	Table ka structure change karne ke liye
DROP	Table ya database delete karne ke liye
TRUNCATE	Table ka pura data delete karne ke liye, bina table delete kiye

ğŸ”¹ 2. DML (Data Manipulation Language)
Data ko modify karne ke liye

Command	Use
INSERT	Table mein naya data add karne ke liye
UPDATE	Existing data ko update karne ke liye
DELETE	Data delete karne ke liye

ğŸ”¹ 3. DQL (Data Query Language)
Data ko retrieve (nikalne) ke liye

Command	Use
SELECT	Table se data fetch karne ke liye

ğŸ”¹ 4. TCL (Transaction Control Language)
Transaction manage karne ke liye

Command	Use
COMMIT	Changes permanently save karne ke liye
ROLLBACK	Changes cancel karne ke liye
SAVEPOINT	Specific point tak rollback karne ke liye
SET TRANSACTION	Transaction ke properties set karne ke liye

ğŸ”¹ 5. DCL (Data Control Language)
Permissions aur access control ke liye

Command	Use
GRANT	Permissions dene ke liye
REVOKE	Permissions wapas lene ke liye

